class ArithmeticOP 
    {

        public static void main(String [] args)
        {
            int a,b,c;
            a=100;
            b=200;
            c=0;
    
            c=a+b;
            System.out.println("The Addition is "+c);
    
            c=a-b;
            System.out.println("The Subtraction is "+c);
    
            c=a*b;
            System.out.println("The Multiplication is "+c);
    
            c=a/b;
            System.out.println("The Division is "+c);

        }

    }    